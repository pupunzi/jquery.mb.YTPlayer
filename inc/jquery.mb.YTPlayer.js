String.prototype.getVideoID = function () {
	var movieURL;
	if (this.substr(0, 16) == "http://youtu.be/") {
		movieURL = this.replace("http://youtu.be/", "");
	} else if (this.indexOf("http") > -1) {
		movieURL = this.match(/[\\?&]v=([^&#]*)/)[1];
	} else {
		movieURL = this
	}
	return movieURL;
};

